#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;

	str = ft_strnew(ft_strlen(s1) + ft_strlen(s2));
	if (str)
	{
		(void)ft_strcat(str, s1);
		(void)ft_strcat(str, s2);
		return (str);
	}
	return (NULL);
}
